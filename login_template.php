		<script type="text/javascript">
			jQuery(function($) {
			 $(document).on('click', '.toolbar a[data-target]', function(e) {
				e.preventDefault();
				var target = $(this).data('target');
				$('.widget-box.visible').removeClass('visible');//hide others
				$(target).addClass('visible');//show target
			 });
			});
			
			
			
			//you don't need this, just used for changing background
			jQuery(function($) {
			 $('#btn-login-dark').on('click', function(e) {
				$('body').attr('class', 'login-layout');
				$('#id-text2').attr('class', 'white');
				$('#id-company-text').attr('class', 'blue');
				
				e.preventDefault();
			 });
			 $('#btn-login-light').on('click', function(e) {
				$('body').attr('class', 'login-layout light-login');
				$('#id-text2').attr('class', 'grey');
				$('#id-company-text').attr('class', 'blue');
				
				e.preventDefault();
			 });
			 $('#btn-login-blur').on('click', function(e) {
				$('body').attr('class', 'login-layout blur-login');
				$('#id-text2').attr('class', 'white');
				$('#id-company-text').attr('class', 'light-blue');
				
				e.preventDefault();
			 });
			 
			});
			
			
		$(function () {
				$('#txtUsuario').focus();
				$("#txtUsuario").keypress(function (e) {
						if (e.which === 13) {
								if ($("#txtUsuario").val() !== "")
								{
										$("#txtPassword").focus();                            
								}
						}
				});

				$("#txtPassword").keypress(function (e) {
						if (e.which === 13) {
								$('#btnIngresar').focus();
								valida();
						}
				});
				valida=function(){
						var txtUsuario = $.trim($('#txtUsuario').val());
						var txtPassword = $.trim($('#txtPassword').val());
						if ((txtUsuario !== "" && txtPassword !== "")) {
								$("#cargando").show();
								$("#btnIngresar").hide();
								login();
						} else {
								alert("Por favor ingrese el usuario y/o contraseña.");
								
								$('#txtUsuario').focus();   
						}
				};
				$('#btnIngresar').on('click', function () {
						valida();
				});
		});
    login = function () {
        
		
			$.post("consultas/consultas_login.php", {
			usr_usuario: $.trim($('#txtUsuario').val()), 
			usr_contrasena: $.trim($('#txtPassword').val()),
					accion: "login"
			},
        function (result) {
             $("#cargando").hide();
             $("#btnIngresar").show();
            if (result !== "") {
                var jsonResult = eval("(" + result + ")");
                if (jsonResult.estatus === "ok") {
                    $(location).attr('href', jsonResult.location);
                } else {
                    if($("#txtUsuario").val()!="")
                    {
                        alert("El usuario y/o contraseña es incorrecta");
                      
                        $('#txtUsuario').focus();
                    }else
                    {
                        alert("Usuario o contraseña incorrecta");
                   
                        $('#txtUsuario').focus();
                    }
                }
            }
        });
    };
			
